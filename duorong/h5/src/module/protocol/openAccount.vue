        <p> 第十四条&nbsp; 客户交易资金子账户指丙方为甲方开立的，管理甲方用于网络投融资交易结算资金的子账户。客户交易资金子账户记载甲方网络投融资交易结算资金的变动明细，并与甲方在丙方登记的甲方银行结算账户和甲方在乙方开立的交易账户建立绑定关系。 </p>
        <p> 第十五条&nbsp; 客户银行结算账户指甲方在商业银行开立的，用于银行资金往来结算的人民币活期账户。若甲方为个人客户，该银行结算账户必须为客户同名借记账户；若甲方为机构客户，则甲方交易资金子账户只能与甲方在商业银行开立的同名银行结算账户（允许是基本结算账户或一般结算账户）之间办理签约转账业务。具体账户信息以甲方通过乙方服务渠道绑定的银行账户信息为准。 </p>
        <p> 甲方充值时可以选择代扣或网银网关方式，当选择代扣方式进行充值时，甲方应先将交易结算资金存入已绑定的银行结算账户，之后才能从已绑定的银行结算账户充值转入交易资金子账户。甲方从交易资金子账户提取的客户交易结算资金，只能通过提现划转回到已绑定的银行结算账户。 </p>
        <p> 第十六条&nbsp; 甲方申请办理网络投融资业务前，需先在乙方开立交易账户。 </p>
