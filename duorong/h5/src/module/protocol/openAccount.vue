        <p> 特别说明：对于甲方是由乙方合作机构推荐的，乙方在取得甲方充分授权（授权乙方可代理其办理账户开立、绑卡、解绑卡、借款、提现、还款等）的情况下，乙方可代甲方在丙方开户。此种情况下，乙方应控制甲方只能作为个人借款客户，甲方授权文件由乙方保留，如丙方需要查阅时，乙方应予以支持。如因乙方未能取得甲方的充分授权而可能导致甲方或丙方等相关方损失的，乙方应承担一切相关责任。 </p>
